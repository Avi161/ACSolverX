"""Direct occurrence-permutation census for Neuwirth rotation systems.

Words are consumed exactly as supplied. Products act right-to-left:
``compose(P, Q)[e] == P[Q[e]]``.
"""

from __future__ import annotations

import hashlib
import itertools
import json
import math
from dataclasses import dataclass

Permutation = tuple[int, ...]
OrderDescriptor = tuple[tuple[str, tuple[int, ...]], ...]
TraceItem = tuple[int, int, int, int, int]
AcceptingOrder = tuple[OrderDescriptor, TraceItem]


def _validate_permutation(p: Permutation) -> None:
    if sorted(p) != list(range(len(p))):
        raise ValueError("expected a permutation of 0, ..., n-1")


def compose(left: Permutation, right: Permutation) -> Permutation:
    """Return the right-to-left product ``left * right``."""
    if len(left) != len(right):
        raise ValueError("permutations must have the same degree")
    _validate_permutation(left)
    _validate_permutation(right)
    return tuple(left[right[e]] for e in range(len(left)))


def _cycle_count(p: Permutation) -> int:
    seen = bytearray(len(p))
    count = 0
    for start in range(len(p)):
        if seen[start]:
            continue
        count += 1
        current = start
        while not seen[current]:
            seen[current] = 1
            current = p[current]
    return count


def cycle_count(p: Permutation) -> int:
    """Count all cycles of ``p``, including fixed points."""
    _validate_permutation(p)
    return _cycle_count(p)


def _orbit_count(generators: tuple[Permutation, ...]) -> int:
    if not generators:
        return 0
    degree = len(generators[0])
    parent = list(range(degree))

    def find(element: int) -> int:
        while parent[element] != element:
            parent[element] = parent[parent[element]]
            element = parent[element]
        return element

    for permutation in generators:
        for element, image in enumerate(permutation):
            left = find(element)
            right = find(image)
            if left != right:
                parent[right] = left
    return sum(find(element) == element for element in range(degree))


def orbit_count(generators: tuple[Permutation, ...]) -> int:
    """Count orbits under the subgroup generated by ``generators``."""
    if not generators:
        return 0
    degree = len(generators[0])
    for permutation in generators:
        if len(permutation) != degree:
            raise ValueError("permutations must have the same degree")
        _validate_permutation(permutation)
    return _orbit_count(generators)


@dataclass(frozen=True)
class OccurrenceData:
    words: tuple[str, ...]
    A: Permutation
    B: Permutation
    positive_ends: dict[str, tuple[int, ...]]

    @classmethod
    def from_words(cls, words: tuple[str, ...]) -> OccurrenceData:
        if not words:
            raise ValueError("at least one nonempty word is required")

        letters: list[str] = []
        word_occurrences: list[tuple[int, ...]] = []
        next_occurrence = 0
        for word in words:
            if not isinstance(word, str) or not word:
                raise ValueError("every word must be a nonempty string")
            if any(not letter.isascii() or not letter.isalpha() for letter in word):
                raise ValueError(f"word contains a nonletter: {word!r}")
            occurrences = tuple(range(next_occurrence, next_occurrence + len(word)))
            word_occurrences.append(occurrences)
            letters.extend(word)
            next_occurrence += len(word)

        degree = 2 * next_occurrence
        A = [-1] * degree
        B = [-1] * degree
        positive_ends_lists: dict[str, list[int]] = {}

        for occurrence, letter in enumerate(letters):
            depart = 2 * occurrence
            arrive = depart + 1
            B[depart] = arrive
            B[arrive] = depart
            generator = letter.lower()
            positive_endpoint = depart if letter.islower() else arrive
            positive_ends_lists.setdefault(generator, []).append(positive_endpoint)

        for occurrences in word_occurrences:
            for position, current in enumerate(occurrences):
                following = occurrences[(position + 1) % len(occurrences)]
                arrive_current = 2 * current + 1
                depart_following = 2 * following
                if A[arrive_current] != -1 or A[depart_following] != -1:
                    raise AssertionError("corner endpoint assigned more than once")
                A[arrive_current] = depart_following
                A[depart_following] = arrive_current

        if -1 in A or -1 in B:
            raise AssertionError("every endpoint must occur in A and B")

        positive_ends = {
            generator: tuple(endpoints)
            for generator, endpoints in sorted(positive_ends_lists.items())
        }
        if any(not endpoints for endpoints in positive_ends.values()):
            raise ValueError("every generator must appear at least once")

        return cls(
            words=tuple(words),
            A=tuple(A),
            B=tuple(B),
            positive_ends=positive_ends,
        )


@dataclass(frozen=True)
class Census:
    words: tuple[str, ...]
    degrees: dict[str, int]
    expected_cases: int
    enumerated_cases: int
    link_components: set[int]
    defect_histogram: dict[int, int]
    minimum_genus: int
    accepting_orders: tuple[AcceptingOrder, ...]
    trace_sha256: str

    def to_json(self) -> dict[str, object]:
        accepting_orders = []
        for descriptor, trace in self.accepting_orders:
            accepting_orders.append(
                {
                    "order": [
                        [generator, list(occurrences)]
                        for generator, occurrences in descriptor
                    ],
                    "link_components": trace[0],
                    "faces": trace[1],
                    "defect": trace[2],
                    "boundary_orbits_BC": trace[3],
                    "boundary_orbits_CB": trace[4],
                }
            )
        return {
            "words": list(self.words),
            "degrees": dict(self.degrees),
            "expected_cases": self.expected_cases,
            "enumerated_cases": self.enumerated_cases,
            "link_components": sorted(self.link_components),
            "defect_histogram": {
                str(defect): count
                for defect, count in sorted(self.defect_histogram.items())
            },
            "minimum_genus": self.minimum_genus,
            "accepting_orders": accepting_orders,
            "trace_sha256": self.trace_sha256,
        }


def _order_options(
    data: OccurrenceData,
) -> tuple[tuple[tuple[int, ...], ...], ...]:
    options = []
    for endpoints in data.positive_ends.values():
        first, *tail = sorted(endpoints)
        options.append(tuple((first, *permutation) for permutation in itertools.permutations(tail)))
    return tuple(options)


def _build_C(data: OccurrenceData, positive_orders: tuple[tuple[int, ...], ...]) -> Permutation:
    successors = [-1] * len(data.A)
    for positive_order in positive_orders:
        negative_order = tuple(data.B[endpoint] for endpoint in reversed(positive_order))
        for cycle in (positive_order, negative_order):
            for current, following in zip(cycle, cycle[1:] + cycle[:1]):
                if successors[current] != -1:
                    raise AssertionError("rotation endpoint assigned more than once")
                successors[current] = following
    if -1 in successors:
        raise AssertionError("every rotation endpoint must receive one successor")
    return tuple(successors)


def _compose(left: Permutation, right: Permutation) -> Permutation:
    return tuple(left[right[element]] for element in range(len(left)))


def enumerate_trace(words: tuple[str, ...]) -> Census:
    """Enumerate every compatible local order and retain its invariant trace."""
    data = OccurrenceData.from_words(words)
    generators = tuple(data.positive_ends)
    options = _order_options(data)
    expected_cases = math.prod(len(generator_options) for generator_options in options)
    cycle_count_A = _cycle_count(data.A)

    trace_digest = hashlib.sha256()
    enumerated_cases = 0
    link_components: set[int] = set()
    defect_histogram: dict[int, int] = {}
    accepting_orders: list[AcceptingOrder] = []

    for positive_orders in itertools.product(*options):
        C = _build_C(data, positive_orders)
        AC = _compose(data.A, C)
        BC = _compose(data.B, C)
        CB = _compose(C, data.B)
        link_component_count = _orbit_count((data.A, C))
        faces = _cycle_count(AC)
        defect = (
            cycle_count_A
            - _cycle_count(C)
            + 2 * link_component_count
            - faces
        )
        boundary_orbits_BC = _orbit_count((AC, BC))
        boundary_orbits_CB = _orbit_count((AC, CB))
        trace_item: TraceItem = (
            link_component_count,
            faces,
            defect,
            boundary_orbits_BC,
            boundary_orbits_CB,
        )
        descriptor: OrderDescriptor = tuple(
            (
                generator,
                tuple(endpoint // 2 for endpoint in positive_order),
            )
            for generator, positive_order in zip(generators, positive_orders)
        )

        canonical_line = json.dumps(
            (descriptor, trace_item),
            ensure_ascii=True,
            separators=(",", ":"),
        )
        trace_digest.update(canonical_line.encode("ascii"))
        trace_digest.update(b"\n")

        enumerated_cases += 1
        link_components.add(link_component_count)
        defect_histogram[defect] = defect_histogram.get(defect, 0) + 1
        if defect == 0:
            accepting_orders.append((descriptor, trace_item))

    if enumerated_cases != expected_cases:
        raise AssertionError("the order census was not exhaustive")
    if any(defect < 0 or defect % 2 for defect in defect_histogram):
        raise AssertionError("surface defects must be nonnegative even integers")

    return Census(
        words=tuple(words),
        degrees={
            generator: len(endpoints)
            for generator, endpoints in data.positive_ends.items()
        },
        expected_cases=expected_cases,
        enumerated_cases=enumerated_cases,
        link_components=link_components,
        defect_histogram=defect_histogram,
        minimum_genus=min(defect_histogram) // 2,
        accepting_orders=tuple(accepting_orders),
        trace_sha256=trace_digest.hexdigest(),
    )
